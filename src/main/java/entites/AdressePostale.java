package entites;

public class AdressePostale {
        int numerorue;
        String libellerue;
        int codepostal;
        String  ville;


        // constructeur
        public AdressePostale(int numerorue, String libellerue, int codepostal, String ville) {
                this.numerorue = numerorue;
                this.libellerue = libellerue;
                this.codepostal = codepostal;
                this.ville = ville;
        }
}

